

// What is Programming :
// Process ot telling computer which task to perform, in order to solve a problem.

// What is Coding :
// Set of instructions that computer can read in binary code (0,1).

// What is Statement :
// Commands that tell computer to do something.

// What is Keyword :
// Keywords are reserved words, that gives special meaning to compiler.

// What is Expression :
// Represents value or something that can be counts as a value like number, String etc

// What is Identifiers :
// Given name's by programmer to define variables, methods and functions
// It must be unique, not same as Keywords

// High-Level Programing Language :
// Easy to learn, user-friendly and uses english like sentence like Java, Dart, c

// Low Level Language :
// Har to learn, not user-friendly and deal with computer hardware components eg. machine and assembly lang.

// What is Compiler :
// Computer program that translates high-level into machine-level language.

// Syntax :
// pattern of write programing language or rule that give concept to code.

// Compile - Time :
// Phase when we writing the code Before Execution [While converting dart code into machine code].

// Run - Time :
// Phase when program is running and executes the code [Here founded error called Run Time errors].